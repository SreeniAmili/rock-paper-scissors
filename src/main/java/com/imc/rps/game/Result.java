package com.imc.rps.game;

/**
 * The Result enum represents the possible outcomes of a round in the Rock-Paper-Scissors game.
 * It is used to indicate whether the player won, lost, or the round ended in a draw.
 *
 * Responsibilities:
 * - Define the possible results: WIN, LOSE, DRAW.
 *
 * Dependencies:
 * - Used in the RockPaperScissorsGame class to determine and display the outcome of a round.
 */
public enum Result {
    WIN, LOSE, DRAW
}
